async def generate_response(service_type: str, user_input: str) -> Optional[str]:
    if not GEMINI_API_KEY:
        return "⚠️ GEMINI_API_KEY не настроен. Обратитесь к администратору."

    template = SYSTEM_PROMPTS.get(service_type, SYSTEM_PROMPTS["smm_post"])
    prompt = template.format(user_input=user_input)

    payload = {
        "contents": [{"parts": [{"text": prompt}]}],
        "generationConfig": {
            "temperature": 0.88,
            "maxOutputTokens": 3072,
            "topP": 0.95,
        }
    }
    try:
        async with aiohttp.ClientSession() as session:
            url = f"{GEMINI_URL}?key={GEMINI_API_KEY}"
            async with session.post(url, json=payload, timeout=aiohttp.ClientTimeout(total=60)) as resp:
                if resp.status == 200:
                    data = await resp.json()
                    return data["candidates"][0]["content"]["parts"][0]["text"]
                error = await resp.text()
                logger.error(f"Gemini {resp.status}: {error}")
                return "❌ Ошибка ИИ-сервиса. Попробуйте позже."
    except Exception as e:
        logger.error(f"AI error: {e}")
        return "❌ Ошибка подключения. Попробуйте ещё раз."
